import pytest
import QR

matrix_01 = [[1, 2], [2, 3]]
matrix_02 = [[2, 3, 0], [1, 2, 0]]


def test_unstable_GrSch():
    assert QR.unstable_GrSch(matrix_01) == ([[0.4472135954999579, 0.8944271909999159], [0.5547001962252291, 0.8320502943378437]], [[2.23606797749979, (3.5777087639996634+0j)], [0, 3.605551275463989]])
    assert QR.unstable_GrSch(matrix_02) == ([[0.5547001962252291, 0.8320502943378437, 0.0], [0.4472135954999579, 0.8944271909999159, 0.0]], [[3.605551275463989, (2.2188007849009166+0j)], [0, 2.23606797749979]])

def test_stable_GrSch():
    assert QR.stable_GrSch(matrix_01) == [[[0.4472135954999579, 0.8944271909999159], [(0.8944271909999162+0j), (-0.4472135954999574+0j)]], [[(2.23606797749979+0j), 0], [(3.5777087639996634+0j), (0.4472135954999579+0j)]]]
    assert QR.stable_GrSch(matrix_02) == [[[0.5547001962252291, 0.8320502943378437, 0.0], [(-0.8320502943378439+0j), (0.5547001962252287+0j), 0j]], [[(3.6055512754639896+0j), 0], [(2.2188007849009166+0j), (0.2773500981126146+0j)]]]


pytest.main()